package ru.ifmo.pga.goodbye.activator.exception;

public class GoodbyeOSGiActivatorException extends Exception {
    public GoodbyeOSGiActivatorException() {
        super();
    }

    public GoodbyeOSGiActivatorException(String message) {
        super(message);
    }

    public GoodbyeOSGiActivatorException(String message, Throwable cause) {
        super(message, cause);
    }

    public GoodbyeOSGiActivatorException(Throwable cause) {
        super(cause);
    }
}
